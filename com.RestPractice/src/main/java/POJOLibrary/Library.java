package POJOLibrary;

public class Library {
	String name;
	String Job;
	
public Library() {
		
		
	}

	public Library(String name, String job) {
		
		this.name = name;
		this.Job = job;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getJob() {
		return Job;
	}

	public void setJob(String job) {
		Job = job;
	}
	
	
	
	

}
